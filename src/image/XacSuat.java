    private int thuanloi = 0;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = JOptionPane.showInputDialog(this, "Nhập số kết quả thuận lợi:");
        try {
            thuanloi = Integer.parseInt(input.trim()); // Lưu vào biến thuanloi
            if (thuanloi < 0) {
                JOptionPane.showMessageDialog(this, "Lỗi: Số kết quả thuận lợi phải >= 0!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            jButton1.setText(String.valueOf(thuanloi)); // Cập nhật nút với số đã nhập
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Lỗi: Hãy nhập số nguyên hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }  // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

