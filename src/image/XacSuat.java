    private double tinhXacSuat(double pA) {
        if (pA < 0 || pA > 1) {
            JOptionPane.showMessageDialog(this, "Lỗi: Xác suất phải từ 0 đến 1!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return -1; // Trả về -1 nếu nhập sai
        }
        return pA * 100; // Chuyển xác suất thành phần trăm
    }

