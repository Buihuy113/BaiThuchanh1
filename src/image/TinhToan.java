    private double calculate(String expression) {
    Stack<Double> numbers = new Stack<>();
    Stack<Character> operators = new Stack<>();
    Stack<Integer> absolutePositions = new Stack<>(); 
    int i = 0;

    // ✅ Thay thế các toán tử đặc biệt trước khi xử lý
    expression = expression.replace("×", "*").replace("÷", "/");

    while (i < expression.length()) {
        char ch = expression.charAt(i);

        // ✅ Xử lý số (bao gồm dấu thập phân)
        if (Character.isDigit(ch) || ch == '.') {
            StringBuilder sb = new StringBuilder();
            while (i < expression.length() && (Character.isDigit(expression.charAt(i)) || expression.charAt(i) == '.')) {
                sb.append(expression.charAt(i++));
            }

            // ✅ Kiểm tra xem sau số có π không (ví dụ: "10π" -> "10 * π")
            if (i < expression.length() && expression.charAt(i) == 'π') {
                numbers.push(Double.parseDouble(sb.toString()) * Math.PI);
                i++;
            } else {
                numbers.push(Double.parseDouble(sb.toString()));
            }
            continue;
        }

        // ✅ Xử lý π (pi) → 3.14
        if (ch == 'π') {
            if (!numbers.isEmpty()) {
                numbers.push(numbers.pop() * Math.PI);
            } else {
                numbers.push(Math.PI);
            }
        }

        // ✅ Xử lý dấu '('
        else if (ch == '(') {
            operators.push(ch);
        } 

        // ✅ Xử lý dấu ')'
        else if (ch == ')') {
            while (!operators.isEmpty() && operators.peek() != '(') {
                numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
            }
            operators.pop();
        } 

        // ✅ Xử lý trị tuyệt đối |...|
        else if (ch == '|') {
            if (!absolutePositions.isEmpty()) {
                int startIndex = absolutePositions.pop();
                double absValue = calculate(expression.substring(startIndex + 1, i)); 
                numbers.push(Math.abs(absValue));
            } else {
                absolutePositions.push(i);
            }
        } 

        // ✅ Xử lý dấu '-' (hỗ trợ `8--9`)
        else if (ch == '-') {
            // Nếu là dấu trừ đầu tiên hoặc sau toán tử khác, thì đây là số âm
            if (i == 0 || (i > 0 && (expression.charAt(i - 1) == '(' || expression.charAt(i - 1) == '|' || 
                                     expression.charAt(i - 1) == '+' || expression.charAt(i - 1) == '-' ||
                                     expression.charAt(i - 1) == '*' || expression.charAt(i - 1) == '/' || expression.charAt(i - 1) == '%'))) {
                StringBuilder sb = new StringBuilder("-");
                i++;
                while (i < expression.length() && (Character.isDigit(expression.charAt(i)) || expression.charAt(i) == '.')) {
                    sb.append(expression.charAt(i++));
                }

                // ✅ Xử lý số âm trước π (ví dụ: "-10π" -> "-10 * π")
                if (i < expression.length() && expression.charAt(i) == 'π') {
                    numbers.push(Double.parseDouble(sb.toString()) * Math.PI);
                    i++;
                } else {
                    numbers.push(Double.parseDouble(sb.toString()));
                }
                continue;
            } 
            
            // Nếu không phải số âm, thì là phép trừ bình thường
            else {
                while (!operators.isEmpty() && precedence(ch) <= precedence(operators.peek())) {
                    numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
                }
                operators.push(ch);
            }
        }

        // ✅ Xử lý toán tử +, *, /, %
        else if (ch == '+' || ch == '*' || ch == '/' || ch == '%') {
            while (!operators.isEmpty() && precedence(ch) <= precedence(operators.peek())) {
                numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
            }
            operators.push(ch);
        }

        i++;
    }

    // ✅ Tính toán với các toán tử còn lại trong stack
    while (!operators.isEmpty()) {
        numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
    }

    return numbers.pop();
}

// ✅ Áp dụng phép toán (+, -, *, /, %)
private double applyOperation(char op, double b, double a) {
    switch (op) {
        case '+': return a + b;
        case '-': return a - b;
        case '*': return a * b;
        case '/': return a / b;
        case '%': return a % b;
    }
    return 0;
}

// ✅ Xác định độ ưu tiên toán tử
private int precedence(char op) {
    if (op == '+' || op == '-') return 1;
    if (op == '*' || op == '/' || op == '%') return 2;
    return 0;
}




