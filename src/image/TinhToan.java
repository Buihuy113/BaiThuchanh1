private double evaluateExpression(String expression) {
    // Xử lý các toán tử đặc biệt trước khi tính toán
    expression = expression.replace("%", "/100"); // Chuyển % thành chia 100
    expression = expression.replace("√", "sqrt"); // Chuyển √ thành "sqrt"
    expression = expression.replaceAll("([\\d\\.]+)²", "pow($1,2)"); // Chuyển x² thành pow(x,2)

    // Hàm tính toán toán học
    return calculate(expression);
}

