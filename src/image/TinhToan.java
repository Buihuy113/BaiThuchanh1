    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String expression = jTextField2.getText(); // Lấy biểu thức
        String result = jTextField3.getText(); // Lấy kết quả
        String timestamp = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()); // Lấy thời gian hiện tại
        String filePath = "lichsu.txt"; // Đường dẫn file

        if (!expression.isEmpty() && !result.isEmpty()) {
            // **Thêm vào JTable**
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{expression, result});

            // **Ghi vào file TXT**
            try {
                FileWriter fw = new FileWriter(filePath, true); // Chế độ ghi tiếp (append)
                BufferedWriter writer = new BufferedWriter(fw);
                writer.write(expression + " = " + result + " (" + timestamp + ")\n");
                writer.close(); // Đóng file để đảm bảo dữ liệu được ghi
                JOptionPane.showMessageDialog(this, "Lưu thành công vào file!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi lưu file: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Biểu thức hoặc kết quả trống!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

